void add_big_ints(struct BigInt *result,
		  struct BigInt *big1,
		  struct BigInt *big2)
{
	struct BigInt *smaller;

	/* ensure big1 >= big2
	 * ================================================================== */
	switch (compare_big_ints(big1, big2)) {

		case NEG:
			/* big1 < big1, swap pointers
			 * ================================================== */
			smaller = big1;
			big1 = big2;
			big2 = smaller;
			break;

		case ZRO:
			/* big1 == big2
			 *
			 *  → big1 + big2
			 *  = 2big1
			 *  = arithmetic shift left by 1 on big1
			 *
			 *  delegate addition to more efficient bitwise
			 *  operation
			 * ================================================== */
			big_int_ash_left(result, big1, 1lu);
			return;

		default:
			/* big1 > big1, do nothing
			 * ================================================== */
			break;
	}

	/* const size_t required; */
	/* expand_big_int() */
}

