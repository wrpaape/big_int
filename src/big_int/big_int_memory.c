/************************************************************************
 *			init_big_int_with_val(1)			*
 *									*
 * Allocates memory for a BigInt and initializes its sign and magnitude	*
 * corresponding to input integer 'init_val'.				*
 ************************************************************************/
struct BigInt *init_big_int_with_val(long long int init_val)
{

	struct BigInt *big_int = init_big_int_with_words(2lu);

	/* assign sign and set first word to magnitude of 'init_val'
	 * ================================================================== */
	if (init_val > 0) {
		big_int->sign	  = POS;
		big_int->words[0] = (unsigned long long int)  init_val;

	} else if (init_val < 0) {
		big_int->sign     = NEG;
		big_int->words[0] = (unsigned long long int) -init_val;

	} else {
		big_int->sign     = ZRO;
		big_int->words[0] = 0llu;
	}

	big_int->num_words = 1u;


	return big_int;
}

