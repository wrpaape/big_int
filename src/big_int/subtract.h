inline void subtract_big_ints(struct BigInt *result,
			      struct BigInt *big1,
			      struct BigInt *big2)
{
	if (big1->sign == big2->sign)
		subtract_big_ints_same_sign(result, big1, big2);
	else
		add_big_ints_same_sign(result, big1, big2);
}
